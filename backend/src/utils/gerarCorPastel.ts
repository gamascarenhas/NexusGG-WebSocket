// Gera uma cor pastel aleatória dentro da família de tons
export function gerarCorPastel(): string {
  const matiz = Math.floor(Math.random() * 60) + 240;
  const saturacao = Math.floor(Math.random() * 20) + 55; // 55–74%
  const luminosidade = Math.floor(Math.random() * 15) + 65; // 65–79%

  return hslParaHex(matiz, saturacao, luminosidade);
}

// Converte HSL para hexadecimal (#rrggbb)
function hslParaHex(h: number, s: number, l: number): string {
  const sNorm = s / 100;
  const lNorm = l / 100;

  const c = (1 - Math.abs(2 * lNorm - 1)) * sNorm;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = lNorm - c / 2;

  let r = 0,
    g = 0,
    b = 0;

  if (h < 60) {
    r = c;
    g = x;
    b = 0;
  } else if (h < 120) {
    r = x;
    g = c;
    b = 0;
  } else if (h < 180) {
    r = 0;
    g = c;
    b = x;
  } else if (h < 240) {
    r = 0;
    g = x;
    b = c;
  } else if (h < 300) {
    r = x;
    g = 0;
    b = c;
  } else {
    r = c;
    g = 0;
    b = x;
  }

  const toHex = (n: number) =>
    Math.round((n + m) * 255)
      .toString(16)
      .padStart(2, "0");

  return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
}
